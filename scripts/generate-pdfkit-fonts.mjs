#!/usr/bin/env node
// Build-time script: reads pdfkit's bundled AFM font metric files and the
// sRGB ICC profile from node_modules, inlines them as string/base64 constants
// in lib/pdfkit-fonts.generated.ts. This lets us serve them from a virtual
// filesystem at runtime on Vercel, where @vercel/nft does not trace pdfkit's
// dynamic `fs.readFileSync(__dirname + '/data/...')` paths through pnpm
// symlinks.
//
// Run automatically as `prebuild` via package.json.

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const DATA_DIR = path.join(ROOT, 'node_modules', 'pdfkit', 'js', 'data');
const OUT = path.join(ROOT, 'lib', 'pdfkit-fonts.generated.ts');

if (!fs.existsSync(DATA_DIR)) {
  console.error(`[generate-pdfkit-fonts] ${DATA_DIR} not found — is pdfkit installed?`);
  process.exit(1);
}

const AFM_FONTS = [
  'Courier', 'Courier-Bold', 'Courier-Oblique', 'Courier-BoldOblique',
  'Helvetica', 'Helvetica-Bold', 'Helvetica-Oblique', 'Helvetica-BoldOblique',
  'Times-Roman', 'Times-Bold', 'Times-Italic', 'Times-BoldItalic',
  'Symbol', 'ZapfDingbats',
];

const ICC_FILE = 'sRGB_IEC61966_2_1.icc';

const afmEntries = AFM_FONTS.map((name) => {
  const filePath = path.join(DATA_DIR, `${name}.afm`);
  if (!fs.existsSync(filePath)) {
    console.error(`[generate-pdfkit-fonts] missing ${filePath}`);
    process.exit(1);
  }
  const contents = fs.readFileSync(filePath, 'utf8');
  return [name, contents];
});

const iccBase64 = fs.existsSync(path.join(DATA_DIR, ICC_FILE))
  ? fs.readFileSync(path.join(DATA_DIR, ICC_FILE)).toString('base64')
  : null;

const header = `// AUTO-GENERATED by scripts/generate-pdfkit-fonts.mjs — DO NOT EDIT.
// Inlines pdfkit's standard AFM font metrics so Vercel serverless bundles
// work without needing pdfkit/js/data/*.afm files on disk.
/* eslint-disable */

export const PDFKIT_AFM_FONTS: Record<string, string> = {
${afmEntries.map(([name, contents]) => `  ${JSON.stringify(name)}: ${JSON.stringify(contents)},`).join('\n')}
};

export const PDFKIT_ICC_BASE64: string | null = ${iccBase64 ? JSON.stringify(iccBase64) : 'null'};
`;

fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, header);

const totalBytes = afmEntries.reduce((s, [, c]) => s + c.length, 0) + (iccBase64?.length || 0);
console.log(`[generate-pdfkit-fonts] wrote ${OUT} — ${AFM_FONTS.length} AFM fonts${iccBase64 ? ' + ICC profile' : ''} (${(totalBytes / 1024).toFixed(1)} KB inlined)`);
